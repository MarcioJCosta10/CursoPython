# Colocar espaço dentro da mascara
teste = input("Digite algo")
print("Olá {:20}".format(teste))

# Colocar alinhamento a esquerda a direita e centralizado e colocar caracteres nas laterais
print("Olá {:<20}".format(teste))
print("Olá {:>20}".format(teste))
print("Olá {:^20}".format(teste))
print("Olá {:=^20}".format(teste))

